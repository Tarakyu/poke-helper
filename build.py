DAMAGE_DENOM = 0.411

def calc_stat(base, pt, nat_eff='none'):
    """포켓몬 챔피언스 HP 외 스탯 실수치.
    계산식: floor(floor((base*2+31+pt*2)*50/100)+5) × 성격보정
    pt: 능력포인트 0~32, nat_eff: 'up'/'down'/'none'
    """
    raw = math.floor(math.floor((base * 2 + 31 + pt * 2) * 50 / 100) + 5)
    if nat_eff == 'up':
        return math.floor(raw * 1.1)
    if nat_eff == 'down':
        return math.floor(raw * 0.9)
    return raw

